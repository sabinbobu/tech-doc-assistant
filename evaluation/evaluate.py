"""
RAG evaluation pipeline using RAGAS.

WHAT THIS DOES:
1. Takes each question from our ground truth dataset
2. Runs it through the full RAG pipeline (retrieval + generation)
3. Collects: question, generated answer, retrieved contexts, ground truth
4. Passes everything to RAGAS for scoring
5. Outputs a report with metric scores

WHY THIS MATTERS IN INTERVIEWS:
Any engineer can build a RAG system. Few can evaluate one rigorously.
Showing up with metrics — "faithfulness: 0.89, answer relevancy: 0.91" —
signals production engineering mindset, not just prototype thinking.

RAGAS METRICS WE MEASURE:
- faithfulness:      Does the answer stay within retrieved context?
                     1.0 = perfect, 0.0 = pure hallucination
- answer_relevancy:  Does the answer address the actual question?
                     1.0 = perfectly on-topic, 0.0 = completely off-topic
- context_precision: Are the retrieved chunks relevant to the question?
                     1.0 = all retrieved chunks are relevant, 0.0 = all noise

RUN WITH:
    uv run python evaluation/evaluate.py

REQUIRES:
    pip install -e ".[eval]"
    OPENAI_API_KEY set in .env
"""

import json
import logging
from datetime import datetime
from pathlib import Path

from dotenv import load_dotenv

load_dotenv()  # Populate os.environ from .env so RAGAS's OpenAI() client finds the key

logging.basicConfig(level=logging.INFO, format="%(name)s - %(message)s")
logger = logging.getLogger(__name__)


def collect_rag_outputs(dataset: list[dict]) -> list[dict]:
    """
    Run each question through the RAG pipeline and collect all outputs
    needed for RAGAS evaluation.

    This separates data collection from scoring — if scoring fails,
    you still have the raw outputs saved to disk for debugging.

    Returns a list of samples, each containing:
    - question
    - answer (generated by your system)
    - contexts (list of retrieved chunk texts)
    - ground_truth (from your dataset)
    """
    # Import here to avoid circular imports and to fail fast if pipeline broken
    from src.embedding.vector_store import query_collection
    from src.generation.generator import generate_answer

    samples = []

    for i, item in enumerate(dataset, 1):
        question = item["question"]
        logger.info(f"Processing question {i}/{len(dataset)}: '{question[:60]}...'")

        # Run retrieval to get the actual contexts used
        raw_chunks = query_collection(question, n_results=5)
        contexts = [chunk["text"] for chunk in raw_chunks]

        # Run full generation
        answer_obj = generate_answer(question, n_results=5)

        samples.append({
            "question": question,
            "answer": answer_obj.answer,
            "contexts": contexts,
            "ground_truth": item["ground_truth"],
            "has_answer": answer_obj.has_answer,
            "category": item.get("category", "general"),
        })

        logger.info(f"  has_answer: {answer_obj.has_answer}")

    return samples


def run_ragas_evaluation(samples: list[dict]) -> dict:
    """
    Run RAGAS metrics on collected samples.

    RAGAS uses an LLM internally to evaluate faithfulness and relevancy —
    this is called "LLM-as-judge" evaluation. The LLM reads your answer
    and the context and scores whether the answer is grounded.

    This costs a small amount in API credits (~$0.05 for 8 questions).

    Returns a dict with metric scores.
    """
    from datasets import Dataset
    from ragas import evaluate
    from ragas.metrics._answer_relevance import answer_relevancy
    from ragas.metrics._context_precision import context_precision
    from ragas.metrics._faithfulness import faithfulness
    from ragas.llms import LangchainLLMWrapper
    from langchain_openai import ChatOpenAI
    from ragas.embeddings import LangchainEmbeddingsWrapper
    from langchain_openai import OpenAIEmbeddings

    logger.info("Running RAGAS evaluation...")

    # RAGAS expects a HuggingFace Dataset with specific column names
    ragas_data = {
        "question": [s["question"] for s in samples],
        "answer": [s["answer"] for s in samples],
        "contexts": [s["contexts"] for s in samples],  # list of lists
        "ground_truth": [s["ground_truth"] for s in samples],
    }

    dataset = Dataset.from_dict(ragas_data)

    # Use gpt-4o as the judge for more accurate evaluation scores
    judge_llm = LangchainLLMWrapper(ChatOpenAI(model="gpt-4o-mini"))
    judge_embeddings = LangchainEmbeddingsWrapper(OpenAIEmbeddings())

    result = evaluate(
        dataset=dataset,
        metrics=[
            faithfulness,        # Does answer stay within retrieved context?
            answer_relevancy,    # Does answer address the question?
            context_precision,   # Are retrieved chunks relevant?
        ],
        llm=judge_llm,
        embeddings=judge_embeddings,
    )

    return result


def _mean_score(scores, key: str) -> float:
    """Extract the mean score for a metric from RAGAS EvaluationResult.

    RAGAS 0.4.x returns per-question lists via result[key], not single floats.
    """
    val = scores[key]
    if isinstance(val, list):
        return sum(val) / len(val) if val else 0.0
    return float(val)


def save_results(samples: list[dict], scores: dict, output_dir: Path) -> Path:
    """
    Save evaluation results to disk with timestamp.

    WHY SAVE RESULTS:
    You want to track how scores change as you improve the system.
    Like keeping a log of firmware versions and their test results.
    If you improve chunking in week 2, you can compare scores to week 1.
    """
    output_dir.mkdir(parents=True, exist_ok=True)
    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    output_path = output_dir / f"eval_results_{timestamp}.json"

    report = {
        "timestamp": timestamp,
        "total_questions": len(samples),
        "scores": {
            "faithfulness": round(_mean_score(scores, "faithfulness"), 4),
            "answer_relevancy": round(_mean_score(scores, "answer_relevancy"), 4),
            "context_precision": round(_mean_score(scores, "context_precision"), 4),
        },
        "per_question": [
            {
                "question": s["question"],
                "category": s["category"],
                "has_answer": s["has_answer"],
                "answer_preview": s["answer"][:200] + "...",
                "contexts_retrieved": len(s["contexts"]),
            }
            for s in samples
        ],
    }

    with open(output_path, "w") as f:
        json.dump(report, f, indent=2)

    logger.info(f"Results saved to: {output_path}")
    return output_path


def print_report(scores: dict, samples: list[dict]) -> None:
    """Print a clean summary report to the terminal."""
    print("\n" + "=" * 55)
    print("  RAG EVALUATION REPORT")
    print("=" * 55)
    print(f"  Questions evaluated:  {len(samples)}")
    print(f"  Questions answered:   {sum(1 for s in samples if s['has_answer'])}")
    print()
    f_score = _mean_score(scores, "faithfulness")
    ar_score = _mean_score(scores, "answer_relevancy")
    cp_score = _mean_score(scores, "context_precision")

    print("  SCORES (0.0 – 1.0, higher is better):")
    print(f"  Faithfulness:       {f_score:.3f}")
    print(f"  Answer Relevancy:   {ar_score:.3f}")
    print(f"  Context Precision:  {cp_score:.3f}")
    print()

    # Flag any low scores — below 0.7 needs attention
    issues = []
    if f_score < 0.7:
        issues.append("⚠️  Low faithfulness — LLM may be hallucinating beyond context")
    if ar_score < 0.7:
        issues.append("⚠️  Low answer relevancy — answers may be off-topic")
    if cp_score < 0.7:
        issues.append("⚠️  Low context precision — retrieval is returning noisy chunks")

    if issues:
        print("  ISSUES DETECTED:")
        for issue in issues:
            print(f"  {issue}")
    else:
        print("  ✅ All scores above 0.7 threshold")

    print("=" * 55)


if __name__ == "__main__":
    from evaluation.dataset import EVALUATION_DATASET

    # Step 1: Collect RAG outputs
    logger.info(f"Evaluating {len(EVALUATION_DATASET)} questions...")
    samples = collect_rag_outputs(EVALUATION_DATASET)

    # Step 2: Score with RAGAS
    scores = run_ragas_evaluation(samples)

    # Step 3: Save and display
    output_path = save_results(
        samples, scores,
        output_dir=Path("evaluation/results")
    )
    print_report(scores, samples)
    print(f"\n  Full results saved to: {output_path}")
